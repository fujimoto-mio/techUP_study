<?php
//　変数aを定義
$a = 7;
//　変数bを定義
 $b = 2;

//　各計算を定義
//　例：$kasan = $⚪︎ +　$△;（加算の例）
$kasan = $a + $b;
//　減算
$gensan = $a - $b;
//　乗算
$josan = $a * $b;
//　除算
$jozan = $a / $b;
 
//　加算結果を出力
echo "加算結果:";
printf($kasan);
//　減算結果を出力
echo "<br>減産結果:";
printf($gensan);
//　乗算結果を出力
echo "<br>乗算:";
printf($josan);
//　除算結果を出力
echo "<br>除算:";
printf($jozan);